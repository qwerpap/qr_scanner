/// Статусы App Tracking Transparency.
/// 
/// Используется для инкапсуляции статусов ATT без зависимости от SDK.
enum AttStatus {
  /// Пользователь разрешил трекинг.
  authorized,

  /// Пользователь запретил трекинг.
  denied,

  /// Трекинг ограничен (например, родительский контроль).
  restricted,

  /// Статус еще не определен (пользователь не видел диалог).
  notDetermined,

  /// ATT не поддерживается (Android или iOS < 14).
  unsupported,
}
